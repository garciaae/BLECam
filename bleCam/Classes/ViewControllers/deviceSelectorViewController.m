#pragma mark - CBCentralManager delegate

-(void)centralManager:(CBCentralManager *)central didConnectPeripheral:(CBPeripheral *)peripheral {
    [peripheral setDelegate:self];
    [peripheral discoverServices:nil];
    self.connected = [NSString stringWithFormat:@"Connected: %@", peripheral.state == CBPeripheralStateConnected ? @"YES" : @"NO"];
    NSLog(@"%@", self.connected);
    if ([self.connected hasSuffix:@"YES"]) {
        [[self bluetoothMark] setImage:[UIImage imageNamed:@"bluetooth-b"]];
    }
}


-(void) centralManager:(CBCentralManager *)central didDisconnectPeripheral:(CBPeripheral *)peripheral error:(NSError *)error{
    [peripheral setDelegate:self];
    [peripheral discoverServices:nil];
    self.connected = [NSString stringWithFormat:@"Connected: %@", peripheral.state == CBPeripheralStateConnected ? @"YES" : @"NO"];
    NSLog(@"%@", self.connected);
    if ([self.connected hasSuffix:@"NO"]) {
        [[self bluetoothMark] setImage:[UIImage imageNamed:@"bluetooth-g"]];
        [cbCentralManager scanForPeripheralsWithServices:nil options:nil];
    }
}


-(void)centralManager:(CBCentralManager *)central didDiscoverPeripheral:(CBPeripheral *)peripheral advertisementData:(NSDictionary *)advertisementData RSSI:(NSNumber *)RSSI {
    NSString *localName = [advertisementData objectForKey:CBAdvertisementDataLocalNameKey];
    
    if ([localName length] > 0) {
        NSLog(@"Found a BLE Device : %@ %@",peripheral, localName);
        [self.cbCentralManager stopScan];
        
        self.sensorTagPeripheral = peripheral;
        peripheral.delegate = self;
        [central connectPeripheral:peripheral options:nil];
    
        [self.nDevices addObject:peripheral];
    }
}


-(void)centralManagerDidUpdateState:(CBCentralManager *)central {
    // Determine the state of the peripheral
    if ([central state] == CBCentralManagerStatePoweredOff) {
        NSLog(@"CoreBluetooth BLE hardware is powered off");
    }
    else if ([central state] == CBCentralManagerStatePoweredOn) {
        NSLog(@"CoreBluetooth BLE hardware is powered on and ready");
        [central scanForPeripheralsWithServices:nil options:nil];
    }
    else if ([central state] == CBCentralManagerStateUnauthorized) {
        NSLog(@"CoreBluetooth BLE state is unauthorized");
    }
    else if ([central state] == CBCentralManagerStateUnknown) {
        NSLog(@"CoreBluetooth BLE state is unknown");
    }
    else if ([central state] == CBCentralManagerStateUnsupported) {
        NSLog(@"CoreBluetooth BLE hardware is unsupported on this platform");
    }
}


#pragma  mark - CBPeripheral delegate

-(void) peripheral:(CBPeripheral *)peripheral didDiscoverServices:(NSError *)error {
    for (CBService *service in peripheral.services) {
        NSLog(@"Discovered service: %@", service.UUID);
        [peripheral discoverCharacteristics:nil forService:service];
    }
}

// Invoked when you discover the characteristics of a specified service.
- (void)peripheral:(CBPeripheral *)peripheral didDiscoverCharacteristicsForService:(CBService *)service error:(NSError *)error
{
    if ([service.UUID isEqual:[CBUUID UUIDWithString:@"FFE0"]])  {  // 1
        for (CBCharacteristic *aChar in service.characteristics)
        {
            // Request key press notification
            if ([aChar.UUID isEqual:[CBUUID UUIDWithString:@"FFE1"]]) { // 2
                [self.sensorTagPeripheral setNotifyValue:YES forCharacteristic:aChar];
                NSLog(@"Found key press characteristic");
            }
        }
    }
}


-(void)peripheral:(CBPeripheral *)peripheral didUpdateValueForCharacteristic:(CBCharacteristic *)characteristic error:(NSError *)error {
    SystemSoundID soundID1;
    // Updated value for key press received
    if ([characteristic.UUID isEqual:[CBUUID UUIDWithString:@"FFE1"]] && *(int*)([characteristic.value bytes]) == 2) {
        // Get the key value
        NSLog(@"Read value %@, %@", characteristic.value, error);
        // Capture an still image
        [self takePhoto];
        AudioServicesCreateSystemSoundID((__bridge CFURLRef)[NSURL fileURLWithPath:[[NSBundle mainBundle]
                                                                                    pathForResource:@"dslr-click" ofType:@"wav"]], &soundID1);
        AudioServicesPlaySystemSound(soundID1);
    }
}


